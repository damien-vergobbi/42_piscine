void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long int	b;

	b = nb;
	if (b < 0)
	{
		ft_putchar('-');
		b = -b;
	}
	if (b >= 10)
		ft_putnbr(b / 10);
	ft_putchar(b % 10 + 48);
}
