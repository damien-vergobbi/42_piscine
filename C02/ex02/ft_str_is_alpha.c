int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'z' || (str[i] > 'Z' && str[i] < 'a'))
			a = 0;
		i++;
	}
	if (i == 0 || a == 1)
		return (1);
	else
		return (0);
}
