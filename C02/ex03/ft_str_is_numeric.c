int	ft_str_is_numeric(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			a = 0;
		i++;
	}
	if (i == 0 || a == 1)
		return (1);
	else
		return (0);
}
