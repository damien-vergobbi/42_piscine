int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'a' || str[i] > 'z')
			a = 0;
		i++;
	}
	if (i == 0 || a == 1)
		return (1);
	else
		return (0);
}
