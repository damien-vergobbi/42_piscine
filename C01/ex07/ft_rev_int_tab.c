void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = size - 1;
	while (i < b)
	{
		a = tab[i];
		tab[i] = tab[b];
		tab[b] = a;
		b--;
		i++;
	}
}
